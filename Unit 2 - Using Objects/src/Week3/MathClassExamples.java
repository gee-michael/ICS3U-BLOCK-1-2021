package Week3;

public class MathClassExamples {
    public static void main(String[] args) {
        double a = -7.8;

        System.out.println(Math.abs(a)); // math is a CLASS. non-static

        System.out.println(Math.sqrt(10));

        double num = Math.pow(2, 4); // Math.pow(base, exponent); -> 16 (returns double)
        System.out.println(num);
    }
}
