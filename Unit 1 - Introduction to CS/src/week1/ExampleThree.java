package week1;

public class ExampleThree {
    public static void main(String[] args) {
        System.out.println("This is \"very\" special!"); //lets quotation marks through
        System.out.println("This\nis\nvery\nspecial!"); //each \n is a new line
        System.out.println("This is \\very special!"); //lets \ through
    }
}
